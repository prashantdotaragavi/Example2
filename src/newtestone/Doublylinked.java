package newtestone;
import java.util.LinkedList;
public class Doublylinked {

	public static void main(String[] args) {
		LinkedList<Integer> x = new LinkedList<Integer>();
		x.add(100);
		x.add(786);
		x.addFirst(10);
		x.add(88);
		x.add(new Integer(577));
		x.add(987);
		x.addLast(20);
System.out.println(x);
	}

}
